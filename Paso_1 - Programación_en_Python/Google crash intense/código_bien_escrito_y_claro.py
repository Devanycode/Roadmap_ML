#Diferencia entre un código bien escrito y uno mal escrito para el lector
def calcular(a):
    b = 3.14
    c = b*(a**2)
    print(c)
calcular(5)

# Código corregido y mejorado para mayor claridad
def calcular_area_circulo(radio):
    pi=3.14
    area=pi*(radio**2)
    print(area)
calcular_area_circulo(5)    
# Aunque el resultado sea el mismo, el segundo código es más claro y fácil de entender.